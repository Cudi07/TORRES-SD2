// Mini-activity1
// Using promp convert Celsius to Kelvin
// Present the output using alert
// Kelvin = Celsius + 273.15
let celsius = prompt("Enter the temperature in Celsius:");
let kelvin = parseFloat(celsius) + 273.15;
alert("The temperature in Kelvin is: " + kelvin);

// Mini-activity2
// Using prompt get circle area
// Present the output using alert
// Area of a circle = π * r^2
let pi = 3.14159;
let radius = prompt("Enter the radius of the circle:");
let area = pi * (parseFloat(radius) ** 2);
alert("The area of the circle is: " + area);
